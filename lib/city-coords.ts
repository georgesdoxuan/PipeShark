/**
 * Returns GPS coords with a small random jitter (3–11 km) so each run
 * targets a different area within the same city.
 */
export function jitterCityCoords(cityName: string): { lat: number; lng: number } | null {
  const key = cityName.trim().toLowerCase();
  const base = CITY_COORDS[key];
  if (!base) return null;

  const MIN_OFFSET = 0.03; // ~3 km
  const MAX_OFFSET = 0.10; // ~11 km
  const sign = () => (Math.random() > 0.5 ? 1 : -1);
  const rand = () => MIN_OFFSET + Math.random() * (MAX_OFFSET - MIN_OFFSET);

  return {
    lat: base.lat + sign() * rand(),
    lng: base.lng + sign() * rand(),
  };
}
